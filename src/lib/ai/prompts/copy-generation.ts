/**
 * 유저 프롬프트 생성 함수
 *
 * @param reviews - 고객 리뷰 문자열 배열
 * @param productName - 선택적 상품명 (제공 시 제목 생성 품질 향상)
 * @returns Claude에 전달할 완성된 유저 프롬프트 문자열
 */
export function buildCopyUserPrompt(
  reviews: string[],
  productName?: string
): string {
  const productLine = productName
    ? `분석 대상 상품명: ${productName}\n\n`
    : "";

  const reviewLines = reviews
    .map((review, idx) => `[리뷰 ${idx + 1}] ${review.trim()}`)
    .join("\n");

  return `${productLine}아래 고객 리뷰들을 분석하고, 시스템 지시사항에 따라 JSON만 출력해 주세요.

${reviewLines}`;
}

