export function toParentCategory(sub: string | null): string {
  if (!sub) return '기타';
  return CATEGORY_MAP[sub] ?? '기타';
}

/** 상위 카테고리에 속하는 모든 세분류 목록 반환 */
export function getSubCategories(parent: string): string[] {
  return Object.entries(CATEGORY_MAP)
    .filter(([, p]) => p === parent)
    .map(([sub]) => sub);
}

